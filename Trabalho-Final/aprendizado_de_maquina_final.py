def logrank_pairwise(df_input, group_col):

    # cópia segura
    df_temp = df_input.copy()

    # verificar coluna
    if group_col not in df_temp.columns:

        print(f"❌ Coluna '{group_col}' não encontrada.")
        return pd.DataFrame()

    # remover nulos
    df_temp = df_temp.dropna(
        subset=['tempo_obito', 'evento', group_col]
    )

    # grupos válidos
    groups = sorted(
        df_temp[group_col].unique()
    )

    results = []

    # comparações
    for i in range(len(groups)):

        for j in range(i + 1, len(groups)):

            g1 = groups[i]
            g2 = groups[j]

            d1 = df_temp[
                df_temp[group_col] == g1
            ]

            d2 = df_temp[
                df_temp[group_col] == g2
            ]

            # evitar grupos muito pequenos
            if len(d1) < 2 or len(d2) < 2:
                continue

            # log-rank
            res = logrank_test(
                d1['tempo_obito'],
                d2['tempo_obito'],
                event_observed_A=d1['evento'],
                event_observed_B=d2['evento']
            )

            results.append({
                'Grupo A': g1,
                'Grupo B': g2,
                'p-value': res.p_value
            })

    return pd.DataFrame(results)


# FUNÇÃO HEATMAP

def plot_logrank_heatmap(df_input, group_col, title):

    # verificar coluna
    if group_col not in df_input.columns:

        print(f"❌ Coluna '{group_col}' não encontrada.")
        return

    # executar log-rank
    res = logrank_pairwise(
        df_input,
        group_col
    )

    # verificar vazio
    if res.empty:

        print(f"⚠️ Nenhum resultado válido para {title}")
        return

    # grupos
    groups = sorted(
        df_input[group_col]
        .dropna()
        .unique()
    )

    # matriz
    mat = pd.DataFrame(
        np.ones((len(groups), len(groups))),
        index=groups,
        columns=groups
    )

    # diagonal
    np.fill_diagonal(mat.values, 0)

    # preencher
    for _, row in res.iterrows():

        g1 = row['Grupo A']
        g2 = row['Grupo B']
        p = row['p-value']

        mat.loc[g1, g2] = p
        mat.loc[g2, g1] = p

    # escala log
    mat_plot = -np.log10(mat + 1e-300)

    # plot
    plt.figure(figsize=(8,6))

    sns.heatmap(
        mat_plot,
        annot=mat,
        fmt='.2e',
        cmap='viridis',
        linewidths=0.5
    )

    plt.title(
        f'Log-rank Test — {title} (-log10 p-value)'
    )

    plt.tight_layout()
