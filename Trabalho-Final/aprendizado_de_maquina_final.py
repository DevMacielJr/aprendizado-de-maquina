def get_col(df, candidates):
    col = next((c for c in candidates if c in df.columns), None)
    if col is None:
        raise ValueError(f"Nenhuma coluna encontrada entre: {candidates}")
    return col
def preparar_base_modelagem(df, features=None, tempo_max=4):
    df = df.copy()

    # localizar coluna de tempo
    tempo_col = get_col(
        df,
        ['IDADE_OBITO', 'IDADE_OBITO_DIAS', 'DIAS_VIDA']
    )

    if 'Obito' not in df.columns:
        raise ValueError("Coluna 'Obito' não encontrada")

    # criar tempo e evento
    df['tempo_obito'] = pd.to_numeric(df[tempo_col], errors='coerce') / 7
    df['evento'] = pd.to_numeric(df['Obito'], errors='coerce')

    # limpeza
    df = df.dropna(subset=['tempo_obito', 'evento'])
    df = df[df['tempo_obito'] > 0]

    # censura administrativa
    df['evento'] = np.where(df['tempo_obito'] > tempo_max, 0, df['evento'])
    df['tempo_obito'] = np.minimum(df['tempo_obito'], tempo_max)

    # features padrão
    if features is None:
        features = ['IDADEMAE', 'PESO', 'SEMAGESTAC', 'APGAR1', 'APGAR5']

    features = [c for c in features if c in df.columns]

    model_data = df[['tempo_obito', 'evento'] + features].copy()
    model_data = model_data.apply(pd.to_numeric, errors='coerce')

    model_data = model_data.fillna(model_data.median(numeric_only=True))
    model_data = model_data.dropna()

    print("✅ Base preparada:", model_data.shape)

    return model_data

def limpar_evento(df, col='Obito'):
    df = df.copy()

    if col not in df.columns:
        raise ValueError("Coluna Obito não existe")

    # converter tudo para string primeiro
    df[col] = df[col].astype(str).str.strip().str.lower()

    mapa = {
        '1': 1,
        '0': 0,
        'true': 1,
        'false': 0,
        'sim': 1,
        'não': 0,
        'nao': 0
    }

    df[col] = df[col].map(mapa)

    # garantir numérico
    df[col] = pd.to_numeric(df[col], errors='coerce')

    # remover inválidos
    df = df.dropna(subset=[col])

    # forçar inteiro binário
    df[col] = df[col].astype(int)

    return df


